async function loadSVGIcons(type) {
  const iconsContainer = document.getElementById("icons-container");

  // Clear any existing icons
  iconsContainer.innerHTML = "";

  // Limit to 5 icons per row
  const iconsPerRow = 5;

  svgIcons.forEach(async (file, index) => {
    try {
      const response = await fetch(`./assets/${file.name}`);
      if (response.ok) {
        const svgContent = await response.text();

        // Create a wrapper div for each icon
        const iconWrapper = document.createElement("div");

        // Apply classes for consistent sizing and layout
        iconWrapper.classList.add(
          "flex",
          "flex-col",
          "items-center",
          "justify-center",
          "py-8",
          "border",
          "border-gray-200",
          "rounded-lg",
          "hover:bg-gray-100",
          "transition-colors"
        );

        // Create a container for the SVG to control its size
        const svgContainer = document.createElement("div");
        svgContainer.classList.add(
          "w-10", // width: 224px
          "h-10", // height: 224p
          "text-blue-400",
          "bg-blue-400"
        );

        // Insert SVG content
        svgContainer.innerHTML = svgContent;

        // Modify the SVG to ensure it fills the container
        const svg = svgContainer.querySelector("svg");
        if (svg) {
          svg.setAttribute("width", "100%");
          svg.setAttribute("height", "100%");

          svg.setAttribute("viewBox", "0 0 24 24"); // Adjust viewBox as needed
        }
        if (type == "stroke") {
          svg.setAttribute("stroke-width", "3");
        } else if (type == "fill") {
          svg.setAttribute("stroke-width", "3");
          svg.setAttribute("fill", "#000");
        }

        // Create a span for the icon name
        const iconName = document.createElement("span");
        iconName.textContent = file.name;
        iconName.classList.add(
          "text-sm",
          "text-center",
          "text-gray-600",
          "mt-2"
        );

        // Assemble the icon wrapper
        iconWrapper.appendChild(svgContainer);
        iconWrapper.appendChild(iconName);

        // Add to container
        iconsContainer.appendChild(iconWrapper);
      } else {
        console.error(`Error loading ${file.name}: ${response.statusText}`);
      }
    } catch (error) {
      console.error(`Failed to fetch ${file.name}:`, error);
    }
  });
}

loadSVGIcons("fill");
